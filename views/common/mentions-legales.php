            <h1 class = "presentation-title2">
            <span>Conditions générales de vente (CGV)</h1>

            <p>Les conditions générales de vente (CGV) encadrent les relations commerciales.</p>
            <p>Elles informent vos clients de leurs droits et obligations lors de la vente de vos produits et/ou de vos services.</p>
            <p>Cette obligation de transparence permet de réduire considérablement le risque de litige entre vos clients et vous-même.</p>
            <p>Le caractère obligatoire et le contenu des conditions générales de vente varient selon que votre offre de biens ou de services
                est adressée à des particuliers ou à des professionnels.</p>

            <h3>Clients particuliers</h3>
            <p>Les CGV doivent figurer obligatoirement sur votre site internet.</p>
            <p>Les conditions générales de vente doivent contenir les informations suivantes :</p>
            <ul>
                <li><i class="fa-solid fa-circle"></i>Caractéristiques essentielles des biens et/ou services</li>
                <li><i class="fa-solid fa-circle"></i>Prix TTC en euros</li>
                <li><i class="fa-solid fa-circle"></i>Frais et date de livraison</li>
                <li><i class="fa-solid fa-circle"></i>Modalités de paiement : modes de paiement autorisés et question du retard de paiement.</li>
                <li><i class="fa-solid fa-circle"></i>Droit de rétractation : délai et conditions pour annuler la commande.</li>
                <li><i class="fa-solid fa-circle"></i>Garantie légale de conformité: titleContent et garantie des vices cachés: titleContent</li>
                <li><i class="fa-solid fa-circle"></i>Service après vente : coût de la communication à distance</li>
                <li><i class="fa-solid fa-circle"></i>Modalités de règlement des litiges : tribunal compétent et possibilité de recourir à un médiateur</li>
            </ul>

            <h3>Clients professionnels</h3>
            <p>Entre professionnels, les CGV sont facultatives, mais elles doivent pouvoir être communiquées
                sur simple demande.</p>
            <p>Le manquement à cette obligation est puni de 75 000 € d'amende.</p>
            <p>Les conditions générales de vente doivent contenir les informations suivantes :</p>
            <ul>
                <li><i class="fa-solid fa-circle"></i>Prix HT en euros : barèmes de prix ou méthode de calcul du prix</li>
                <li><i class="fa-solid fa-circle"></i>Rabais et ristourne : réductions de prix, remises promotionnelles ponctuelles et ristournes différées. Elles doivent être fixées selon des critères précis et objectifs.</li>
                <li><i class="fa-solid fa-circle"></i>Escomptes commerciaux : réduction accordée à un client en cas de paiement anticipé</li>
                <li><i class="fa-solid fa-circle"></i>Modalités de paiement : modes de paiement autorisés et pénalités appliquées en cas de retard</li>
                <li><i class="fa-solid fa-circle"></i>Frais et date de livraison</li>
                <li><i class="fa-solid fa-circle"></i>Modalités de règlement des litiges : tribunal compétent</li>
            </ul>

            <p>Vous avez également la possibilité d'ajouter une <em>clause de réserve de propriété</em>,
                une clause de limitation de responsabilité, une clause relative aux cas de <em>force majeure</em>
                ou encore les conditions de résiliation du contrat.</p>

            <h4>À noter</h4>
            <p>vous avez le droit d'imposer des CGV distinctes pour chaque catégorie de clients (grossistes, détaillants...). Dans ce cas, les clients d'une catégorie ne peuvent exiger la communication que des seules CGV qui les concernent.</p>
